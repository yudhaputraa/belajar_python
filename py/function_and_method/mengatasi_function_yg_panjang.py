def contoh_nama_fungsi_yang_sangat_panjang(lst):
    for i in lst:
        print(i, end=' ')

# contoh manggil fungsi yg repot
contoh_nama_fungsi_yang_sangat_panjang(['python','ruby','java'])

#cara mempermudah
tulis = contoh_nama_fungsi_yang_sangat_panjang
tulis(['python','ruby','java','php','javascript'])